#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	t_list	*temp;

	if (!lst || !f)
		return ;
	while (lst != NULL)
	{
		temp = lst;
		lst = lst->next;
		f(temp->content);
	}
}
